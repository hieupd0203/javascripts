// Loại 1 : Arithmectic Operators( toán tử số học)
let number1 = 10;
let number2 = 20;

// + operator
const add = number1 + number2;
console.log("add:", add);

// - operator
const sub = number1 - number2;
console.log("sub:", sub);

// * operator
const multiple = number1 * number2;
console.log("multipe:", multiple);

// ** operator
const expo = number1 ** number2;
console.log("expo:", expo);

// / operator
const division = number1 / number2;
console.log("division:", division);

// % operator
const modulus = number1 % number2;
console.log("modulus:", modulus);

// ++ operator
let increment = ++number1;
console.log("increment:", increment);
// -- operator
let decrement = --number2;
console.log("decrement:", decrement);

